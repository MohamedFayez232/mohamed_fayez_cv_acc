const STORAGE_KEY = "cv-lang";

function renderLedger(containerId, entries){
  const el = document.getElementById(containerId);
  el.innerHTML = entries.map(item => `
    <div class="ledger-row">
      <div class="ledger-row__dates">
        <span>${item.dates[0]}</span>
        <span>— ${item.dates[1]}</span>
      </div>
      <div>
        <p class="ledger-row__role">${item.role}</p>
        <p class="ledger-row__org">${item.org}</p>
        ${item.points.length ? `<ul class="ledger-row__points">${item.points.map(p => `<li>${p}</li>`).join("")}</ul>` : ""}
      </div>
    </div>
  `).join("");
}

function renderChips(containerId, items){
  const el = document.getElementById(containerId);
  el.innerHTML = items.map(t => `<li>${t}</li>`).join("");
}

function applyLanguage(lang){
  const d = DATA[lang];
  const html = document.documentElement;
  html.setAttribute("lang", d.htmlLang);
  html.setAttribute("dir", d.dir);
  document.title = d.pageTitle;

  document.querySelectorAll("[data-i18n]").forEach(node => {
    const key = node.getAttribute("data-i18n");
    if (d[key] !== undefined) node.textContent = d[key];
  });

  document.getElementById("stampTextPath").textContent = d.stampLine;
  document.getElementById("stampCenter1").textContent = d.stampCenter1;
  document.getElementById("stampCenter2").textContent = d.stampCenter2;

  renderLedger("experienceLedger", d.experience);
  renderLedger("educationLedger", d.education);
  renderChips("technicalSkillsList", d.technicalSkills);
  renderChips("personalSkillsList", d.personalSkills);
  renderChips("languagesList", d.languages);

  document.querySelectorAll(".lang-toggle__opt").forEach(opt => {
    opt.classList.toggle("is-active", opt.dataset.lang === lang);
  });

  try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
}

function initLang(){
  let saved = null;
  try { saved = localStorage.getItem(STORAGE_KEY); } catch (e) {}
  const browserLang = (navigator.language || "ar").toLowerCase().startsWith("ar") ? "ar" : "en";
  applyLanguage(saved || browserLang);
}

document.getElementById("langToggle").addEventListener("click", () => {
  const current = document.documentElement.getAttribute("lang");
  applyLanguage(current === "ar" ? "en" : "ar");
});

document.getElementById("printBtn").addEventListener("click", () => window.print());

initLang();
